package sk.ness.game.model;

public class Score {

	private int time;
	
	private String game;

	public Score(){
		
	}
	
	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

	public String getGame() {
		return game;
	}

	public void setGame(String game) {
		this.game = game;
	}
	
}
